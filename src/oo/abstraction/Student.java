package oo.abstraction;

public class Student {
	int seatnumber;
	String school;
	String major;
	int idnumber;
	String name;
	String gender;
	int grade;
	int location;
	public Student(String school, String major, int idnumber, String name, String gender, int grade){
		this.school = school;
		this.major = major;
		this.idnumber = idnumber;
		this.name = name;
		this.gender = gender;
		this.grade = grade;
	}
}

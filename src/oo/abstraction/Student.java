package oo.abstraction;

public class Student {
	String school;
	String major;
	int idnumber;
	String name;
	String gender;
	int grade;
	int credit;
	public Student(String school, String major, int idnumber, String name, String gender, int grade, int credit){
		this.school = school;
		this.major = major;
		this.idnumber = idnumber;
		this.name = name;
		this.gender = gender;
		this.grade = grade;
		this.credit= credit;
	}
	
}

package oo.abstraction;

public class EMR {
	int ChartNumber;
	String IdentityCard;
	String Name;
	char Gender;
	int Birthday;
	public EMR(int ChartNumber, String IdentityCard, String Name, char Gender, int Birthday){
		this.ChartNumber = ChartNumber;
		this.IdentityCard = IdentityCard;
		this.Name = Name;
		this.Gender = Gender;
		this.Birthday = Birthday;
	}
}
